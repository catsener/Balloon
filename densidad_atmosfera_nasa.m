function rho=densidad_atmosfera_nasa(h)
    if h<11000
        T=15.04-0.00649*h;
        p=101.29*((T+273.1)/288.08)^5.256;
    elseif h>25000
        T=-131.21+0.00299*h;
        p=2.488*((T+273.1)/216.6)^-11.388;
    else
        T=-56.46+h*0;
        p=22.65*exp(1.73-0.000157*h);
    end
    rho=p/(0.2869*(T+273.1));
end